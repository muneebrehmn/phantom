"""
phantom/payloads/adaptive.py

Adaptive Payload Engine — Phantom's core differentiator.

Every other open-source prompt injection tool (Garak, PyRIT, promptmap)
fires a static payload library and reports what hit.  Phantom's adaptive
engine closes the loop: it observes the target's defence, synthesises
novel bypass payloads via an LLM, and iterates until it breaks through
or exhausts its budget.

This is the loop that red teamers run manually today. Nobody has automated
it in an open framework.

────────────────────────────────────────────────────────────────────────────
THE LOOP
────────────────────────────────────────────────────────────────────────────

  Phase A — PROBE
    Fire a canary payload. Observe the raw response.
    Classify what defence mechanism is running:
      · KeywordFilter    — refuses specific words / phrases
      · ConstitutionalAI — refuses based on inferred intent
      · OutputGuard      — post-generation filter (Llama Guard, etc.)
      · TopicClassifier  — refuses off-topic categories
      · RateLimiter      — 429 / retry-after signals
      · NoDefence        — surface accepted the canary

  Phase B — SYNTHESISE
    Call the Anthropic API with:
      · Detected defence type + raw refusal text
      · Original attack goal (e.g. "leak system prompt")
      · Surface type (chatbox / API / search)
      · History of what has already been tried and failed
    The LLM returns N candidate bypass payloads as structured JSON.

  Phase C — RANK + MUTATE
    Score candidates by predicted bypass probability (returned by the LLM).
    Apply existing mutation transforms (encoding, fragmentation, evasion)
    to the top-K candidates for additional coverage.

  Phase D — FIRE + OBSERVE
    Fire top-K candidates via the normal PayloadEngine.
    If any succeed → emit AdaptiveFinding, stop.
    If all fail → record observations, go back to Phase B (next round).

  Budget: max_rounds (default 3), candidates_per_round (default 5).
  Total maximum payload budget = max_rounds × candidates_per_round = 15.

────────────────────────────────────────────────────────────────────────────
DESIGN DECISIONS
────────────────────────────────────────────────────────────────────────────

· The adaptive engine is a layer ABOVE the payload engine, not a
  replacement.  It calls engine.run() just like phantom.py does.

· Defence classification happens locally (regex + heuristics) — no API
  call needed for Phase A.  This keeps the probe cheap.

· The synthesis prompt (Phase B) never asks the LLM to produce harmful
  content directly — it asks for *security research payloads for testing
  AI robustness*, framed correctly for the research context.

· All synthesised payloads are stored in AdaptiveSession so the final
  report can show the full attack chain, not just the winning payload.

· The engine is opt-in: it only activates when config.adaptive_attack=True
  and config.anthropic_api_key is set.  Zero behaviour change for users
  who don't configure it.
"""

