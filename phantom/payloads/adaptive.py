# ---------------------------------------------------------------------------
# Defence taxonomy
# ---------------------------------------------------------------------------

class DefenceType(Enum):
    """
    Detected defence mechanism type — drives the synthesis strategy.

    Each type maps to a different bypass approach in the synthesis prompt.
    The classifier (below) maps refusal text → DefenceType using heuristics.
    """
    KEYWORD_FILTER     = "keyword_filter"      # Refuses on specific words
    CONSTITUTIONAL_AI  = "constitutional_ai"   # Refuses on inferred intent
    OUTPUT_GUARD       = "output_guard"        # Post-generation filter
    TOPIC_CLASSIFIER   = "topic_classifier"    # Off-topic refusal
    RATE_LIMITER       = "rate_limiter"        # 429 / backoff
    UNKNOWN            = "unknown"             # Could not classify
    NO_DEFENCE         = "no_defence"          # Canary accepted


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------

@dataclass
class SynthesisedPayload:
    """
    A payload generated by the LLM synthesis step (Phase B).

    Carries enough metadata to explain the attack chain in the final report.
    """
    text: str
    strategy: str           # Human-readable name of the bypass approach used
    predicted_score: float  # 0.0–1.0 LLM's own confidence estimate
    reasoning: str          # Why this should work against the detected defence
    round_number: int       # Which adaptive round produced this


@dataclass
class AdaptiveRound:
    """
    Record of one complete adaptive round (Phases B–D).

    Stored in AdaptiveSession so the report can show the full attack chain.
    """
    round_number: int
    detected_defence: DefenceType
    refusal_text: str           # Raw text that was classified
    synthesised: List[SynthesisedPayload] = field(default_factory=list)
    results: List[Optional[PayloadResult]] = field(default_factory=list)
    breakthrough: bool = False  # True if any payload in this round succeeded
    winning_payload: Optional[SynthesisedPayload] = None


@dataclass
class AdaptiveSession:
    """
    Complete record of one adaptive attack session against one surface.

    This is what the report builder consumes — it shows exactly how Phantom
    escalated from probe → defence detection → synthesis → breakthrough.
    """
    surface_url: str
    surface_type: str
    attack_goal: str            # e.g. "leak system prompt"
    started_at: float = field(default_factory=time.time)
    rounds: List[AdaptiveRound] = field(default_factory=list)
    succeeded: bool = False
    finding: Optional[Finding] = None
    total_payloads_fired: int = 0
    elapsed_seconds: float = 0.0

    def summary(self) -> str:
        status = "BREAKTHROUGH" if self.succeeded else "EXHAUSTED"
        return (
            f"AdaptiveSession [{status}] "
            f"surface={self.surface_url} "
            f"rounds={len(self.rounds)} "
            f"payloads={self.total_payloads_fired} "
            f"elapsed={self.elapsed_seconds:.1f}s"
        )


# ---------------------------------------------------------------------------
# Defence classifier (Phase A)
# ---------------------------------------------------------------------------

# Regex patterns → DefenceType  (ordered: first match wins)
_DEFENCE_PATTERNS: List[Tuple[re.Pattern, DefenceType]] = [
    # Rate limiting
    (re.compile(r"rate.?limit|too many requests|retry.after|slow down", re.I), DefenceType.RATE_LIMITER),
    # Constitutional AI — intent-based refusals with ethical language
    (re.compile(
        r"(potential|could be|might be).{0,40}(harm|dangerous|misuse)|"
        r"(ethical|safety|responsible).{0,30}(concern|reason|guideline)|"
        r"designed to (prevent|avoid|protect)",
        re.I
    ), DefenceType.CONSTITUTIONAL_AI),
    # Output guard — post-generation catch phrases
    (re.compile(
        r"(content|output|response).{0,30}(filter|guard|blocked|flagged)|"
        r"(llama.?guard|perspective.?api|moderat)",
        re.I
    ), DefenceType.OUTPUT_GUARD),
    # Topic classifier — off-topic refusals
    (re.compile(
        r"(outside|beyond|not within).{0,30}(scope|topic|domain|purpose)|"
        r"(only|designed to|here to).{0,30}(help with|answer|assist with)",
        re.I
    ), DefenceType.TOPIC_CLASSIFIER),
    # Keyword filter — lists specific words or is very short/generic
    (re.compile(
        r"(contain|includes?|detected).{0,30}(prohibited|forbidden|restricted|keyword)|"
        r"(word|phrase|term).{0,20}not allowed",
        re.I
    ), DefenceType.KEYWORD_FILTER),
]

# If refusal text is very short and generic, it's likely a keyword filter
_GENERIC_REFUSAL_RE = re.compile(
    r"^(i('m| am) (sorry|unable)|i can('t| not)|that('s| is) not something)",
    re.I
)


def classify_defence(
    response_text: str,
    status_code: int,
    latency: float,
) -> DefenceType:
    """
    Classify what defence mechanism produced this refusal.

    Uses heuristics only — no API call.  Fast enough to run on every response.

    Args:
        response_text: Raw response body from the target.
        status_code:   HTTP status of the response.
        latency:       Round-trip time in seconds.

    Returns:
        DefenceType enum value.
    """
    if status_code == 429:
        return DefenceType.RATE_LIMITER

    # Empty or very short response with non-200 → likely hard block
    if not response_text.strip() or (status_code >= 400 and len(response_text) < 50):
        return DefenceType.KEYWORD_FILTER

    text_lower = response_text.lower()

    for pattern, defence_type in _DEFENCE_PATTERNS:
        if pattern.search(text_lower):
            return defence_type

    # Short generic refusal → keyword filter (surface didn't explain why)
    if len(response_text.strip()) < 150 and _GENERIC_REFUSAL_RE.search(response_text.strip()):
        return DefenceType.KEYWORD_FILTER

    # Has refusal language but we couldn't classify further
    return DefenceType.UNKNOWN


